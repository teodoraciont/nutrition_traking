// Helpers
export function getLast7Avg(field) {
  const last7 = ENTRIES.slice(-7).filter(e => e[field] != null)
  if (!last7.length) return 0
  return Math.round(last7.reduce((s, e) => s + e[field], 0) / last7.length)
}

export function getLatestWeight() {
  const withWeight = [...ENTRIES].reverse().find(e => e.weight != null)
  return withWeight ? withWeight.weight : null
}

export function getWeightAvg7d() {
  const last7 = ENTRIES.slice(-7).filter(e => e.weight != null)
  if (!last7.length) return getLatestWeight()
  return Math.round((last7.reduce((s, e) => s + e.weight, 0) / last7.length) * 10) / 10
}

export function getGymSessionsThisWeek() {
  const weekEntries = ENTRIES.filter(e => e.date >= '2026-05-25' && e.date <= '2026-05-31')
  return weekEntries.filter(e => e.gym).length
}

export function getDaysSinceStart() {
  const start = new Date(ENTRIES[0].date)
  const today = new Date('2026-05-28')
  return Math.floor((today - start) / (1000 * 60 * 60 * 24))
}
